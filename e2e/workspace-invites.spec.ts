import { test, expect } from '@playwright/test';
import { gotoTasksClean } from './helpers';

/**
 * E2E Wave B (PR-b-04 «UI приглашений»).
 *
 * ─────────────────────────────────────────────────────────────────────────────
 * ВАЖНО: happy-path приглашения (owner приглашает → приглашённый принимает →
 * пространство появляется у обоих) НЕ запускается в текущем e2e-харнессе и помечен
 * `test.fixme`.
 *
 * Причины (харнесс = локальный sql.js + `?e2e=1`, БЕЗ реального бэкенда):
 *   1. Инвайты живут ТОЛЬКО на сервере (таблица sync_workspace_invites не входит
 *      в SQLite-зеркало/outbox) и меняются исключительно через SECURITY DEFINER
 *      RPC (invite_to_workspace/accept_invite/…). В `?e2e=1` Supabase-клиент не
 *      ходит в сеть — RPC некому обслужить.
 *   2. Нужны ДВА реальных пользователя (owner + приглашённый) с валидными TF-ID,
 *      а e2e-байпас логинит одного фиктивного free-юзера.
 *   3. shared-пространство в e2e даже не создать (free-план + нет серверного
 *      триггера), а инвайты возможны только в shared-ws.
 *
 * Что уже покрыто без бэкенда (unit / RTL, `npm test`):
 *   • src/lib/invites.test.ts — контракт 4 RPC + маппинг всех кодов ошибок;
 *   • src/components/InviteMemberModal.test.tsx — валидация TF-ID, happy-path,
 *     перевод кодов ошибок;
 *   • src/components/MembersTab.test.tsx — ролевой гейт, promote/demote/remove,
 *     секция приглашений + отзыв;
 *   • src/components/MyInvitesSection.test.tsx — рендер входящих, accept →
 *     switchWorkspace, лимит-гард, reject.
 *
 * TODO(backend-e2e): когда появится сид на 2 пользователей против настоящего
 * Supabase (staging), снять `fixme` и реализовать шаги ниже. Хак с прямой
 * записью в sync_workspace_invites сознательно НЕ пишем — он проверял бы фикстуру,
 * а не продукт.
 * ─────────────────────────────────────────────────────────────────────────────
 */
test.describe('workspace invites', () => {
  test.fixme('happy-path: owner приглашает → приглашённый принимает', async ({ page }) => {
    // Требует реального бэкенда и двух пользователей (см. заголовок файла).
    //
    // Ожидаемый сценарий (для будущего backend-e2e):
    //   [owner]
    //   1. Открыть shared-пространство → /workspace-settings → вкладка «Участники».
    //   2. «Пригласить» → ввести TF-ID приглашённого → роль editor → отправить.
    //   3. Инвайт появляется в секции «Приглашения» со статусом «Ожидает».
    //   [приглашённый, другая сессия]
    //   4. В сайдбаре видна секция «Приглашения» с бейджем-счётчиком.
    //   5. «Принять» → тост «Вы приняли приглашение» → активное пространство
    //      переключилось на общее.
    //   6. У приглашённого пространство доступно в переключателе (после sync).
    await gotoTasksClean(page);
    await expect(page.locator('[data-onboarding="sidebar"]')).toBeVisible();
  });

  /**
   * Smoke без бэкенда: секция «Мои приглашения» НЕ протекает в UI, когда
   * входящих инвайтов нет (типичный e2e-стейт — локальный free-юзер).
   */
  test('нет входящих приглашений → секция скрыта', async ({ page }) => {
    await gotoTasksClean(page);
    await expect(page.getByTestId('my-invites')).toHaveCount(0);
  });
});
